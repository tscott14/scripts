function get_daily_prolouge() {
	local MONTH=$(date -d @$1 +%b)
	local DAY=$(date -d @$1 +%d)
	echo "$(get_prologue $MONTH $DAY)"
	
}

CAL_DIFF=1123200
CURRENT_NOW=$(date +%s)
NEW_CAL_NOW=$(($CURRENT_NOW - 0))
OLD_CAL_NOW=$(($CURRENT_NOW - $CAL_DIFF))

case $1 in
	'new') 	echo "$(get_daily_prolouge $NEW_CAL_NOW)" && exit;;
	'old') 	echo "$(get_daily_prolouge $OLD_CAL_NOW)" && exit;;
	'') 	echo 'please supply argument for new or old calendar. [new/old]'
		echo './script new	or	./script old' && exit 1 ;;
	*) 	echo 'Valid options are "new" or "old"!' && exit 1 ;;
esac








